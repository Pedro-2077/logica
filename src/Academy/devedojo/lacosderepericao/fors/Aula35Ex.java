public class Aula35Ex {
    public static void main(String[] args) {

        for (int i = 0; i <=10 ; i++) {
            System.out.println("FAzendo a tabuada do numero "+i);
            for (int j = 0; j <=10 ; j++) {
                System.out.println(i+"x"+j+"="+i*j);
            }
            System.out.println("---------------------------");
        }
    }
}
